document.addEventListener('DOMContentLoaded', function() {
    const form = document.getElementById("login-form");
    const msg = document.getElementById("login-message");
    const btnLogin = form.querySelector('button[type="submit"]');

    // Verificar se j√° est√° logado
    if (window.sistemaAuth && window.sistemaAuth.verificarAutenticacao()) {
        console.log('‚úÖ Usu√°rio j√° logado, redirecionando...');
        window.location.href = 'index.html';
        return;
    }

    // Executar diagn√≥stico ao carregar a p√°gina
    console.log('üîß Iniciando diagn√≥stico do sistema...');
    diagnosticoCompleto().then(resultado => {
        if (resultado.success) {
            console.log('‚úÖ Diagn√≥stico conclu√≠do com sucesso');
            // N√£o mostrar mensagem de sistema pronto (mais profissional)
        } else {
            console.error('‚ùå Diagn√≥stico falhou:', resultado.error);
            // N√£o mostrar erro para o usu√°rio final (mais profissional)
        }
    });

    form.addEventListener("submit", async (e) => {
        e.preventDefault();
        
        // Resetar mensagem
        msg.style.display = "none";
        msg.textContent = "";
        btnLogin.disabled = true;
        btnLogin.textContent = "Entrando...";
        btnLogin.style.opacity = "0.7";

        const username = document.getElementById("username").value.trim();
        const password = document.getElementById("password").value;

        // Valida√ß√£o b√°sica
        if (!username || !password) {
            mostrarMensagem("Por favor, preencha todos os campos", "error");
            btnLogin.disabled = false;
            btnLogin.textContent = "Entrar";
            btnLogin.style.opacity = "1";
            return;
        }

        try {
            console.log('üîê Tentando login para:', username);
            const resultado = await window.sistemaAuth.fazerLogin(username, password);
            
            if (resultado.success) {
                mostrarMensagem("Login realizado com sucesso! Redirecionando...", "success");
                console.log('‚úÖ Login bem-sucedido para:', username);
                
                setTimeout(() => {
                    window.location.href = "index.html";
                }, 1500);
            } else {
                throw new Error(resultado.error);
            }
            
        } catch (err) {
            console.error("‚ùå Erro no login:", err);
            mostrarMensagem("Usu√°rio ou senha incorretos", "error");
        } finally {
            btnLogin.disabled = false;
            btnLogin.textContent = "Entrar";
            btnLogin.style.opacity = "1";
        }
    });

    function mostrarMensagem(texto, tipo, tempo = 5000) {
        msg.style.display = "block";
        msg.className = "message " + tipo;
        msg.innerHTML = texto;
        
        setTimeout(() => {
            msg.style.display = "none";
        }, tempo);
    }

    // REMOVIDO: Preenchimento autom√°tico para desenvolvimento
});

